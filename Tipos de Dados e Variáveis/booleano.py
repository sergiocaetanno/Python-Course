"""
Valores possíveis:

True -> verdadeiro
False -> falso

Obs.: Sempre começa com letra maiúscula. "true" e "false" estão incorretos.
"""

print(type(True))

#Operações básicas: 

#Negação (not):

estado = not True
print(estado)

#Disjunção (or):

print(True or True)
print(True or False)
print(False or True)
print(False or False)

#Conjunção (and):

print(True and True)
print(True and False)
print(False and True)
print(False and False)
